       4.52187689813627263E+04,    -2.49983048181120962E+03, &
       2.43805296995560639E+01,     3.28446985307203782E+06, &
      -1.97068191184322269E+07,     5.09526024926646422E+07, &
      -7.41051482115326577E+07,     6.63445122747290267E+07, &
