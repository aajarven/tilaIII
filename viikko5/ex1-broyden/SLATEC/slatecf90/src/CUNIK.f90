  if (INIT /= 0) go to 40
!-----------------------------------------------------------------------
!     INITIALIZE ALL VARIABLES
!-----------------------------------------------------------------------
  RFN = 1.0E0/FNU
  CRFN = CMPLX(RFN,0.0E0)
!     T = ZR*CRFN
!-----------------------------------------------------------------------
!     OVERFLOW TEST (ZR/FNU TOO SMALL)
!-----------------------------------------------------------------------
  TSTR = REAL(ZR)
  TSTI = AIMAG(ZR)
  TEST = R1MACH(1)*1.0E+3
  AC = FNU*TEST
  if (ABS(TSTR) > AC .OR. ABS(TSTI) > AC) go to 15
  AC = 2.0E0*ABS(ALOG(TEST))+FNU
  ZETA1 = CMPLX(AC,0.0E0)
  ZETA2 = CMPLX(FNU,0.0E0)
  PHI=CONE
  return
   15 CONTINUE
  T=ZR*CRFN
  S = CONE + T*T
  SR = CSQRT(S)
  CFN = CMPLX(FNU,0.0E0)
  ZN = (CONE+SR)/T
  ZETA1 = CFN*CLOG(ZN)
  ZETA2 = CFN*SR
  T = CONE/SR
  SR = T*CRFN
  CWRK(16) = CSQRT(SR)
  PHI = CWRK(16)*CON(IKFLG)
  if (IPMTR /= 0) RETURN
  T2 = CONE/S
  CWRK(1) = CONE
  CRFN = CONE
  AC = 1.0E0
  L = 1
  DO 20 K=2,15
    S = CZERO
    DO 10 J=1,K
      L = L + 1
      S = S*T2 + CMPLX(C(L),0.0E0)
   10   CONTINUE
    CRFN = CRFN*SR
    CWRK(K) = CRFN*S
    AC = AC*RFN
    TSTR = REAL(CWRK(K))
    TSTI = AIMAG(CWRK(K))
    TEST = ABS(TSTR) + ABS(TSTI)
    if (AC < TOL .AND. TEST < TOL) go to 30
   20 CONTINUE
  K = 15
   30 CONTINUE
  INIT = K
   40 CONTINUE
  if (IKFLG == 2) go to 60
!-----------------------------------------------------------------------
!     COMPUTE SUM FOR THE I FUNCTION
!-----------------------------------------------------------------------
  S = CZERO
  DO 50 I=1,INIT
    S = S + CWRK(I)
   50 CONTINUE
  SUM = S
  PHI = CWRK(16)*CON(1)
  return
   60 CONTINUE
!-----------------------------------------------------------------------
!     COMPUTE SUM FOR THE K FUNCTION
!-----------------------------------------------------------------------
  S = CZERO
  T = CONE
  DO 70 I=1,INIT
    S = S + T*CWRK(I)
    T = -T
   70 CONTINUE
  SUM = S
  PHI = CWRK(16)*CON(2)
  return
end
