!***FIRST EXECUTABLE STATEMENT  GAMLN
  IERR=0
  if (Z <= 0.0E0) go to 70
  if (Z > 101.0E0) go to 10
  NZ = Z
  FZ = Z - NZ
  if (FZ > 0.0E0) go to 10
  if (NZ > 100) go to 10
  GAMLN = GLN(NZ)
  return
   10 CONTINUE
  WDTOL = R1MACH(4)
  WDTOL = MAX(WDTOL,0.5E-18)
  I1M = I1MACH(11)
  RLN = R1MACH(5)*I1M
  FLN = MIN(RLN,20.0E0)
  FLN = MAX(FLN,3.0E0)
  FLN = FLN - 3.0E0
  ZM = 1.8000E0 + 0.3875E0*FLN
  MZ = ZM + 1
  ZMIN = MZ
  ZDMY = Z
  ZINC = 0.0E0
  if (Z >= ZMIN) go to 20
  ZINC = ZMIN - NZ
  ZDMY = Z + ZINC
   20 CONTINUE
  ZP = 1.0E0/ZDMY
  T1 = CF(1)*ZP
  S = T1
  if (ZP < WDTOL) go to 40
  ZSQ = ZP*ZP
  TST = T1*WDTOL
  DO 30 K=2,22
    ZP = ZP*ZSQ
    TRM = CF(K)*ZP
    if (ABS(TRM) < TST) go to 40
    S = S + TRM
   30 CONTINUE
   40 CONTINUE
  if (ZINC /= 0.0E0) go to 50
  TLG = ALOG(Z)
  GAMLN = Z*(TLG-1.0E0) + 0.5E0*(CON-TLG) + S
  return
   50 CONTINUE
  ZP = 1.0E0
  NZ = ZINC
  DO 60 I=1,NZ
    ZP = ZP*(Z+(I-1))
   60 CONTINUE
  TLG = ALOG(ZDMY)
  GAMLN = ZDMY*(TLG-1.0E0) - ALOG(ZP) + 0.5E0*(CON-TLG) + S
  return
!
!
   70 CONTINUE
  GAMLN = R1MACH(2)
  IERR=1
  return
end
