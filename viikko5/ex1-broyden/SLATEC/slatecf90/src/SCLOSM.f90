subroutine SCLOSM (IPAGE)
!
!! SCLOSM is subsidiary to SPLP.
!
!***LIBRARY   SLATEC
!***TYPE      ALL (SCLOSM-A)
!***AUTHOR  (UNKNOWN)
!***DESCRIPTION
!
!     1. UNLOAD, RELEASE, OR CLOSE UNIT NUMBER IPAGEF.
!
!***SEE ALSO  SPLP
!***ROUTINES CALLED  XERMSG
!***REVISION HISTORY  (YYMMDD)
!   811215  DATE WRITTEN
!   890605  Corrected references to XERRWV.  (WRB)
!   891214  Prologue converted to Version 4.0 format.  (BAB)
!   900402  Added TYPE section.  (WRB)
!   900510  Convert XERRWV calls to XERMSG calls.  (RWC)
!***END PROLOGUE  SCLOSM
  CHARACTER*8 XERN1
!
!***FIRST EXECUTABLE STATEMENT  SCLOSM
  IPAGEF=IPAGE
  CLOSE(UNIT=IPAGEF,IOSTAT=IOS,ERR=100,STATUS='KEEP')
  return
!
  100 WRITE (XERN1, '(I8)') IOS
  call XERMSG ('SLATEC', 'SCLOSM', &
     'IN SPLP, CLOSE HAS ERROR FLAG = ' // XERN1, 100, 1)
  return
end
