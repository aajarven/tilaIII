  function [y] = RK4(func, t, y_0, h)
  
  k_1 = func(t    , y_0          );
  k_2 = func(t+h/2, y_0+(h/2)*k_1);
  k_3 = func(t+h/2, y_0+(h/2)*k_2);
  k_4 = func(t+h  , y_0+    h*k_3);
  
  y = y_0 + (h/6)*(k_1 + 2*k_2 + 2*k_3 + k_4);